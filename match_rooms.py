with app.app_context():
    db_teachers = {t.full_name: t.id for t in Teacher.query.all()}
    db_rooms = {r.number: r.id for r in Room.query.all()}

    print('=' * 100)
    print(f'{"#":3s} | {"Здание":6s} | {"Каб":5s} | {"Преподаватель (PDF)":40s} | {"В базе":20s} | {"room_id":7s} | {"teacher_id":10s}')
    print('=' * 100)

    missing_rooms = set()
    missing_teachers = set()
    matched = []

    for i, (bldg, room_num, subj, teacher_pdf) in enumerate(PDF_DATA, 1):
        short_name = NAME_MAP.get(teacher_pdf)
        teacher_id = db_teachers.get(short_name) if short_name else None
        room_id = db_rooms.get(room_num)

        t_status = f'id={teacher_id}' if teacher_id else ('SKIP' if not short_name else 'NOT FOUND')
        r_status = f'id={room_id}' if room_id else 'MISSING'

        if not room_id and room_num:
            missing_rooms.add((bldg, room_num))
        if short_name and not teacher_id:
            missing_teachers.add(short_name)

        print(f'{i:3d} | {bldg:6s} | {room_num:5s} | {teacher_pdf[:40]:40s} | {(short_name or "-"):20s} | {r_status:7s} | {t_status:10s}')

        if teacher_id and room_id:
            matched.append((teacher_id, short_name, room_id, room_num, bldg))

    print()
    if missing_rooms:
        print(f'MISSING ROOMS (need to create): {sorted(missing_rooms)}')
    if missing_teachers:
        print(f'MISSING TEACHERS: {sorted(missing_teachers)}')

    print()
    print('=== TEACHER -> ROOM MAPPING (matched) ===')
    # Deduplicate: one teacher may have multiple rooms
    teacher_rooms = {}
    for tid, tname, rid, rnum, bldg in matched:
        if tid not in teacher_rooms:
            teacher_rooms[tid] = []
        teacher_rooms[tid].append((rid, rnum, bldg))

    for tid in sorted(teacher_rooms.keys()):
        tname = [m[1] for m in matched if m[0] == tid][0]
        rooms_list = teacher_rooms[tid]
        rooms_str = ', '.join(f'{bldg}-{rnum}(id={rid})' for rid, rnum, bldg in rooms_list)
        print(f'  {tname:25s} (id={tid:2d}) -> {rooms_str}')
